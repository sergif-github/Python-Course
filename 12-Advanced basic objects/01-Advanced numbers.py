# ADVANCED NUMBERS
print('''ADVANCED NUMBERS''')

print("Hexadecimal")
print("Hex() function can convert numbers into a hexadecimal format")
print("Hex(246):", hex(246))
print("Hex(512):", hex(512))

print("\nBinary")
print("Bin() function can convert numbers into a binary format")
print("Bin(1234):", bin(1234))
print("Bin(128):", bin(128))
print("Bin(512):", bin(512))

print("\nExponentials")
print("Pow() takes two arguments, equivalent to x^y. With three arguments it is equivalent to (x^y)%z")
print("Pow(3,4):", pow(3, 4))
print("Pow(3,4,5):", pow(3, 4, 5))

print("\nAbsolute")
print("Abs() returns the absolute value of a number")
print("Abs(-3.14):", abs(-3.14))

print("\nRound")
print("Round() will round a number to a given precision in decimal digits (default 0 digits)")
print("Round(3,2):", round(3, 2))
print("Round(3.1415926535,2):", round(3.1415926535, 2))
print("Round(395,-2):", round(395, -2))
